    getCitaion() {
        const randomCitation = this.cytaty[Math.floor(Math.random() * this.cytaty.length)];

        const maxLength = 50
         const words: string[] = randomCitation.split(' ');
  const lines: string[] = [];
  let currentLine: string = '';

  words.forEach(word => {
    // Jeśli dodanie tego słowa przekroczy max długość, zapisz obecną linię
    if (currentLine.length + word.length + 1 > maxLength) {
      lines.push(currentLine);
      currentLine = word; // Zaczynamy nową linię z obecnym słowem
    } else {
      // Jeśli zmieści się, dodajemy słowo do obecnej linii
      currentLine += (currentLine ? ' ' : '') + word;
    }
  });

  // Dodaj ostatnią linię, jeśli jest jakaś
  if (currentLine.length > 0) {
    lines.push(currentLine);
  }

        return lines.join('\n')
    }

